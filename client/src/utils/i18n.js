let currentLanguage = localStorage.getItem('app_language') || 'es'

export const setLanguage = (lang) => {
  if (translations[lang]) {
    currentLanguage = lang
    localStorage.setItem('app_language', lang)
    document.documentElement.lang = lang
  }
}

export const getLanguage = () => currentLanguage

export const t = (key, fallback = key) => {
  return translations[currentLanguage]?.[key] || translations['es']?.[key] || fallback
}

// Initialize
if (typeof window !== 'undefined') {
  const savedLang = localStorage.getItem('app_language') || 'es'
  setLanguage(savedLang)
}

export default { t, setLanguage, getLanguage }

